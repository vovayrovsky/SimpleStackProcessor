#ifndef CS_GUARD
#define CS_GUARD

#define OPCODE_SHIFT 10     //OPCODE length is 6

                    //FULL 2-Byte:
#define PUSH 0x0    //0x00
#define POP  0x1    //0x04
#define DUP  0x2    //0x08
#define SWAP 0x3    //0x0C

#define JMP  0x10   //0x40
#define JE   0x11   //0x44

#define ADD  0x20   //0x80
#define SUB  0x21   //0x84
#define MUL  0x22   //0x88
#define DIV  0x23   //0x8C

#define MOV  0x30   //0xC0
#define MSR  0x31   //0xC4
#define PSR  0x32   //0xC8
#define PPC  0x33   //0xCC

#define WINT 0x3E   //0xF8
#define HLT  0x3F   //0xFC

#endif
